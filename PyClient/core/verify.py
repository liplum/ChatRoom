_regex_port = re.compile('^([0-9]|[1-9]\d{1,3}|[1-5]\d{4}|6[0-4]\d{3}|65[0-4]\d{2}|655[0-2]\d|6553[0-5])$')

_regex_account = re.compile('^[a-zA-Z0-9_]{3,16}$')

_regex_password = re.compile('^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[a-zA-Z\d]{8,}$')


def ip(t: str) -> bool:
    if t == "":
        return False
    return re.match(_regex_hostname, t) is not None


def port(t: str) -> bool:
    if t == "":
        return False
    return re.match(_regex_port, t) is not None


def account(t: str) -> bool:
    """
    a-z A-Z 0-9 and _
    """
    if t == "":
        return False
    return re.match(_regex_account, t) is not None


def password(t: str) -> bool:
    """
    at least 8 characters, at least 1 number and both lower and uppercase letters
    """
    if t == "":
        return False
    return re.match(_regex_password, t) is not None
